def get_health_insight(dataset_title: str, dataset_description: str | None) -> dict | None:
    """Return the matching health insight for a dataset, or None."""
    text = f"{dataset_title or ''} {dataset_description or ''}".lower()

    for keyword, insight in HEALTH_INSIGHT_MAP.items():
        if keyword in text:
            return insight

    general_health_terms = [
        "health",
        "disease",
        "mortality",
        "morbidity",
        "hospital",
        "clinic",
        "ghs",
        "nhia",
        "medical",
    ]
    if any(term in text for term in general_health_terms):
        return GENERAL_HEALTH_INSIGHT

    return None
